package S1_Loop3;

public class Forloop5
{
    //startNum - 5
    //endNum   - 1
    //incr/decr - decr by 1


    public static void main(String[] args) {

        //      5    0>=1       0
        for(int i=5; i>=1; i--)
        {
            System.out.println(i);        //5 4 3 2 1
        }
    }

}
