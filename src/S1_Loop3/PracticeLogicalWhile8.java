package S1_Loop3;

public class PracticeLogicalWhile8
{
    public static void main(String[] args)
    {
        int i = 1;
        while (i <= 10)
        {
            System.out.println("2 x " + i + " = " + (2 * i));
            i++;
        }

        for (int j = 1; j <= 10; j++)
        {
            System.out.println("11 x " + j + " = " + (11 * j));
        }




    }
}
