package S1_Conditional4;

public class PracticeElseIf12
{
    public static void main(String[] args)
    {
        int temp = 35;

        if (temp >= 40)
        {
            System.out.println("Very Hot");
        }
        else if (temp >= 30)
        {
            System.out.println("Hot");
        } else
        {
            System.out.println("Normal");
        }


    }
}
