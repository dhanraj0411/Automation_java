package S1_Conditional4;

public class PracticeSwitchCas17
{
    public static void main(String[] args)
    {
        int choice = 2;

        switch (choice)
        {
            case 1:
                System.out.println("Tea");
                break;
            case 2:
                System.out.println("Coffee");
                break;
            case 3:
                System.out.println("Juice");
                break;
            default:
                System.out.println("Wrong choice");
        }

    }
}
