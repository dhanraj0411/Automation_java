package S1_Conditional4;

public class PracticeElseIf10
{
    public static void main(String[] args)
    {
        int marks = 91;

        if (marks >= 90)
        {
            System.out.println("Grade A");
        } else if (marks >= 70)
        {
            System.out.println("Grade B");
        } else
        {
            System.out.println("Grade C");
        }


    }
}
